package com.comp0103.verification;

public class Fibonacci {

    static long[] generateFibonacci(int n) {
        long[] fib = new long[n];
        fib[0] = 0;
        fib[1] = 1;
        for (int i = 2; i < n; i++) {
            fib[i] = fib[i - 1] + fib[i - 2];
        }
        return fib;
    }
}
